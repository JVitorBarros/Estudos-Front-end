let n1 = 10.5;
let n2 = 10.5;

if(n1 == n2 ){
    console.log(`Os números contem os mesmos valores`)
    
}else if(n1 > n2){
    console.log(`Maior: ${n1}`)
    console.log(`Menor: ${n2}`)
}else{
    console.log(`Maior: ${n2}`)
    console.log(`Menor: ${n1}`)
    
}